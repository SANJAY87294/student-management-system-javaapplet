void loadNoticeData(){
  
     String sql="select * from notice ";       
     try{
            pst=conn.prepareStatement(sql);                         
            rs=pst.executeQuery();
            while(NoticeTab.getRowCount() > 0 )
            {((DefaultTableModel)NoticeTab.getModel()).removeRow(0);
            }
            int col = rs.getMetaData().getColumnCount();
            while(rs.next())
            {
                Object [] rows = new Object[col];
                for(int i=1;i<=col;i++)
                {    rows[0]=rs.getObject("sno");  
                     rows[1]=rs.getObject("date");
                     rows[2]=rs.getObject("submitted_by");                                            
                     rows[3]=rs.getObject("last_date");
                     rows[4]=rs.getObject("subject");
                     rows[5]=rs.getObject("info");
                     break;   
            }                      
            ((DefaultTableModel)NoticeTab.getModel()).insertRow(rs.getRow( ) -1 , rows);
            }                           
            rs.close();
            pst.close();
         } catch (Exception ex) {
         } 
    }


