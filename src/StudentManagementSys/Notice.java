    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
          int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to Send Notice ?","Add Notice",JOptionPane.YES_NO_OPTION);
        if(p==0){          
            try {
                String sql ="INSERT INTO  notice (date,"
                        + "last_date,subject,submitted_by,info) "
                        + "VALUES (? , ? , ? , ? , ? )";
                pst=conn.prepareStatement(sql);
                SimpleDateFormat sdf1=new SimpleDateFormat("dd-MM-yyyy");               
                Date date=new Date();
                pst.setString(1,sdf1.format(date));
                                
                SimpleDateFormat sdf2=new SimpleDateFormat("dd-MM-yyyy");               
                pst.setString(2,sdf2.format(LastDate.getDate()));
                
                pst.setString(3,Subject.getText());
                pst.setString(4,User.getText());
                pst.setString(5,Info.getText());    
                
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data is saved successfully");
                rs.close();
                pst.close();                   
                loadNoticeData();                   
            }
            catch (Exception e)  { }
            }
    }//GEN-LAST:event_SubmitActionPerformed

